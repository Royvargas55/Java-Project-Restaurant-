    private void btnRealizarReservaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRealizarReservaActionPerformed
        // TODO add your handling code here:
        String id = ID.getText();
        String nombre = Nombre.getText();
        String telefono = Telefono.getText();
        String nAcom = Nacompanantes.getText();
        //Se verifica si los campos estan completos
        if (id.equals("") || nombre.equals("") || telefono.equals("") || nAcom.equals("")) {
            status.setText("DEBE DE COMPLETAR LOS ESPACIOS");
        } else {
            //Se llama al metodo para realizar la reserva
            Reservacion reservar = new Reservacion();
            reservar.HacerReserva(ID, Nombre, Telefono, Nacompanantes, status);
            setText();
        }

    }//GEN-LAST:event_btnRealizarReservaActionPerformed

