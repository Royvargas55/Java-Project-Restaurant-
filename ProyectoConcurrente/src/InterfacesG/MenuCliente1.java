    private void btn_verificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_verificarActionPerformed
        jTextArea1.setText("\n");
        if (txt_norden.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite un número de orden");
        } else {
            //Se llama al metodo para consultar el pedido solicitado
            p.ConsultarPedido(txt_norden, jTextArea1);
            for (Iterator<Pedido> iterator = Pedido.ListaPedidos.iterator(); iterator.hasNext();) {
                Pedido pedidos = iterator.next();
                //Si el pedido que buscamos lo encontramos, se elimina
                if (pedidos.toString().contains(txt_norden.getText())) {
                    int ID1 = pedidos.getID();
                    txt_id.setText(String.valueOf(ID1));
                }
            }
            //Se habilita el boton de pagar despues de verificar
            btn_pagar.setEnabled(true);
        }

    }//GEN-LAST:event_btn_verificarActionPerformed

